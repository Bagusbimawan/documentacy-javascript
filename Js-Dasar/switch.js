// switch

let day = 5;

switch (day) {
  case 1:
    console.log("Ini hari senin");
    break;
  case 2:
    console.log("ini hari selasa");
    break;
  case 3:
    console.log("ini hari rabu");
    break;
  case 4:
    console.log("ini hari kamis");
    break;
  default:
    console.log("invalid day");
    break;
}
